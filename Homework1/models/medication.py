class Medication:
    def __init__(self, id, name, for_disease, type, dosage, ingredients,  administration, adverse_effects):
        self.id = id
        self.name = name
        self.for_disease = for_disease
        self.type = type
        self.dosage = dosage
        self.ingredients = ingredients
        self.administration = administration
        self.adverse_effects = adverse_effects

